const a = 7
let b = 3

b += a //[Atribuição aditiva], que pode ser entedido pela seguinte maneira: (b = b + a)
console.log(b)

b -= 4 // [Atribuição subtrativa], (b = b - 4);
console.log(b)

b *= 2 // [Atribuição multiplicativa], (b = b * 3);
console.log(b)

b /= 3 // [Atribuição divisiva], (b = b / 3);
console.log(b)

b %= 2 // [Atribuição modular], (b = b % 2);
console.log(b)